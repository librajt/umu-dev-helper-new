// 最长公共子串
function longestCommonSubstring (str1, str2) {
    var size1 = str1.length;
    var size2 = str2.length;
    if (size1 == 0 || size2 == 0) return 0;
 
    // the start position of substring in original string
    var start1 = -1;
    var start2 = -1;
    // the longest length of common substring 
    var longest = 0; 
 
    // record how many comparisons the solution did;
    // it can be used to know which algorithm is better
    var comparisons = 0;
 
    for (var i = 0; i < size1; ++i)
    {
        var m = i;
        var n = 0;
        var length = 0;
        while(m < size1 && n < size2)
        {
            ++comparisons;
            if (str1[m] != str2[n])
            {
                length = 0;
            }
            else 
            {
                ++length;
                if (longest < length)
                {
                    longest = length;
                    start1 = m-longest+1;
                    start2 = n-longest+1;
                }
            }
 
            ++m;
            ++n;
        }
    }
 
    // shift string2 to find the longest common substring
    for (var j = 1; j < size2; ++j)
    {
        var m = 0;
        var n = j;
        var length = 0;
        while(m < size1 && n < size2)
        {
            ++comparisons;
            if (str1[m] != str2[n])
            {
                length = 0;
            }
            else 
            {
                ++length;
                if (longest < length)
                {
                    longest = length;
                    start1 = m-longest+1;
                    start2 = n-longest+1;
                }
            }
 
            ++m;
            ++n;
        }
    }
 
// console.log( "(first, second, comparisions) = (" + start1 + ", " + start2 + ", " + comparisons + ")");

    return longest;
}

function isSimilary(str1, str2) {
    if (!str1 || !str2) {
        return 0;
    }
    var lcs = longestCommonSubstring(str1,str2);
    var size1 = str1.length;
    var size2 = str2.length;
    var s = size1 >= size2 ? str1 : str2
    var similaryValue = (lcs / s.length).toFixed(2);
    return similaryValue;

}

