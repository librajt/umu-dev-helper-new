function findCnWord(word) {

    if (!word) {
        return '';
    }

    var langKey = '';
    var match = {
        key:'',
        similary: 0
    };

// 去掉前后空格
    word = word.replace(/^\s+|\s+$/g, '');
// 去掉内容中的html标签
    word = word.replace(/<[^>]*>/g, '');

    if (!word) {
        return '';
    }

    for (var key in ja_jp) {
        var str = ja_jp[key].replace(/<[^>]*>/g, '');
        var similary = isSimilary(str, word);
        if (match.similary < similary) {
            match.similary = similary;
            match.key = key;
        }
    }

    langKey = match.key;

    if (!langKey) {
        return word;
    }

    return zh_cn[langKey] + ' --准确度：'+match.similary + ' --langKey：'+langKey;
}

// 如果子元素没有文本则显示
function haveOtherText(node) {

    var hasTextNode = false;

    if (node && node.hasChildNodes()) {
        for (var i = 0; i < node.childNodes.length; i++) {
            if ((node.childNodes[i].nodeType === 1||node.childNodes[i].nodeType === 3) && !!node.childNodes[i].textContent) {
                hasTextNode = true;
                break;
            }
        }
    }

    return hasTextNode;
}

// 如果子节点没有文本,取改元素的部分文字
function getElmPartText(node) {
    var word = '';
    if (node && node.hasChildNodes()) {
        for (var i = 0; i < node.childNodes.length; i++) {
            if (node.childNodes[i].nodeType === 3) {
                word += node.childNodes[i].textContent;
                break;
            }
        }
    }

    if (!word) {
        word = node.innerText;
    }

    return word;
}



